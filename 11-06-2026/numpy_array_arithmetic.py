import numpy as np


first_array = np.array([10, 20, 30, 40])
second_array = np.array([1, 2, 3, 4])

print("First Array:")
print(first_array)

print("\nSecond Array:")
print(second_array)

print("\nArithmetic Operations:")
print("Addition:", first_array + second_array)
print("Subtraction:", first_array - second_array)
print("Multiplication:", first_array * second_array)
print("Division:", first_array / second_array)
print("Remainder:", first_array % second_array)
print("Power:", second_array ** 2)

print("\nScalar Operations:")
print("Add 5:", first_array + 5)
print("Multiply by 3:", first_array * 3)
